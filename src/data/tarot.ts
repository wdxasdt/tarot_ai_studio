      const uprightGeneral = `【${originalName}正位】代表在${suit.name}的领域（关于${suit.theme}）注入了像【${rank.name}】代表的${rank.keywordUp}的能量。这是一个积极的征兆，暗示你应当保持主控、顺应这种富有成效的流动。`;
